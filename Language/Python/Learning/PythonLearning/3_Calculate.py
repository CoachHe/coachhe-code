# 逻辑运算符
a = 10
b = 20
if a and b:
    print("a and b = true")
else:
    print("a and b = false")

# 成员运算符
list = [1, 2, 3, 4, 5]
if a in list:
    print("a in list")
else:
    print("a is not in list")
a = 2
if a in list:
    print("a in list")
else:
    print("a is not in list")
